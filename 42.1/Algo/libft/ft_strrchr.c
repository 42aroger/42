#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ch;

	ch = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			ch = (char *)s;
		s++;
	}
	if (*s == c)
		ch = (char *)s;
	return (ch);
}
